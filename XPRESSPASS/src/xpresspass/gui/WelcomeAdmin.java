    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

         empId=txtEmpIdLog.getText();
       pwd=String.valueOf(txtPwdLog.getPassword());
        if(empId.isEmpty()||pwd.isEmpty())
        {
             JOptionPane.showMessageDialog(null, "Please enter data into all the fields", "Oops!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
          AdminPojo adminPojo=new AdminPojo();
          adminPojo.setEmp_id(empId);
          adminPojo.setPwd(pwd);
          AdminPojo admPojo=AdminDAO.logInAdmin(adminPojo);
          if(admPojo!=null)
            {
                name=admPojo.getName();
                JOptionPane.showMessageDialog(null,"Successfully logged In!\nWelcome Dr. "+name+" \nHave a great day!","Success!",JOptionPane.INFORMATION_MESSAGE,icon);
                
                GatePassAdmin g = new GatePassAdmin(name);
                g.setVisible(true);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null,"LogIn denied!","ERROR!",JOptionPane.ERROR_MESSAGE);
                return;
            }

       
        }
        
        catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Some error occured while connecting to DataBase", "ERROR!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

