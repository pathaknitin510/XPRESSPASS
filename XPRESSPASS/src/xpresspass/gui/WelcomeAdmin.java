    private int validateInput() {
        name = txtName.getText().trim();
        empId = txtEmpIdReg.getText().trim();
        email = txtEmail.getText().trim();
        college = String.valueOf(jcbCollege.getSelectedItem());

        pwd = String.valueOf(txtPwdReg.getPassword());
        cPwd = String.valueOf(txtConfPwdReg.getPassword());
        if (name.isEmpty() || empId.isEmpty() || email.isEmpty() || pwd.isEmpty()
                || cPwd.isEmpty() || college.isEmpty()) {
            return 0;
        }
        if (email.contains("@gmail.com") == false) {
            return -1;
        }
        if (pwd.equals(cPwd) == false) {
            return -2;
        }
        return 1;
    }

